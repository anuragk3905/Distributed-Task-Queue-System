import mongoose from "mongoose";

const taskSchema=new mongoose.Schema({
jobId:{
type:String,
unique:true,
sparse:true,
index:true,
},
type:{
type:String,
required:true,
index:true,
},
queue:{
type:String,
required:true,
},
status:{
type:String,
enum:[
"queued",
"active",
"completed",
"failed",
"cancelled",
"dead",
],
default:"queued",
index:true,
},
priority:{
type:Number,
default:3,
},
payload:{
type:mongoose.Schema.Types.Mixed,
},
result:{
type:mongoose.Schema.Types.Mixed,
},
error:{
type:String,
},
progress:{
type:Number,
default:0,
},
attempts:{
type:Number,
default:0,
},
maxAttempts:{
type:Number,
default:5,
},
createdBy:{
type:String,
},
completedAt:{
type:Date,
},
scheduledAt:{
type:Date,
},
isRecurring:{
type:Boolean,
default:false,
index:true,
},
cronPattern:{
type:String,
},
repeatJobKey:{
type:String,
},
flowId:{
type:String,
index:true,
},
parentJobId:{
type:String,
},
workflowName:{
type:String,
},
isFlowJob:{
type:Boolean,
default:false,
},
},{
timestamps:true,
});

taskSchema.index(
{ completedAt:1 },
{ expireAfterSeconds:2592000 }
);

taskSchema.index({
queue:1,
status:1,
createdAt:-1,
});

export const Task=
mongoose.model(
"Task",
taskSchema
);